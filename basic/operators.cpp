#include<iostream>
using namespace std;
int main(){
    int a=4,b=6;
    cout<<"operator in c++\n";
    cout<<"following are the type of operators in c++"<<endl;
    //Arithmatics operators
    cout<<"the value of a+b is"<<a+b<<endl;
    cout<<"the value of a-b is"<<a-b<<endl;
    cout<<"the value of a*b is"<<a*b<<endl;
    cout<<"the value of a/b is"<<a/b<<endl;
    cout<<"the value of a%b is"<<a%b<<endl;
    cout<<"the value of a++ is"<<a++<<endl;
    cout<<"the value of a-- is"<<a--<<endl;
    cout<<"the value of ++a is"<<++a<<endl;
    cout<<"the value of --a is"<<--a<<endl;
    cout<<"the value of b++ is"<<b++<<endl;
    cout<<"the value of b-- is"<<b--<<endl;
    cout<<"the value of ++b is "<<++b<<endl;
    cout<<"the value of --b is "<<--b<<endl; 
    return 0;

}